
public class _4_Person {
	private String name;
	private int age;
	private String jobTitle;

	public _4_Person(String name, int age, String jobTitle) {
		this.name = name;
		this.age = age;
		this.jobTitle = jobTitle;
	}

	public String toString() {
		return name + " " + age + " " + jobTitle;
	}
}
